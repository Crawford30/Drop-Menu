import Foundation
import UIKit

class Utilities {
    
    
    
    static func  categoryArray() -> Array<String> {
        
        var categoryTitles:[String] = []
        
        let sortedCat = categoryTitles.sorted()
        
        return sortedCat
        
    
    }
    
    
    
    static func servicesArray() -> Array<String> {
        
        var serviceTitles:[String]  = []
        
        let sortedServices = serviceTitles.sorted()
        
        return sortedServices
    }
    
    
    
    
    

   

}
